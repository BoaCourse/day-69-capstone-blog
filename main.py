@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        data = db.session.execute(db.select(User).where(User.email == form.email.data))
        check_user = data.scalar()
        if check_user:
            flash("You've already signed up with that email, log in instead!")
            return redirect(url_for("login"))
        hashed_password = generate_password_hash(password=request.form.get("password"),
                                                 method="pbkdf2:sha256:600000",
                                                 salt_length=8)
        new_user = User(
            email=request.form.get("email"),
            password=hashed_password,
            name=request.form.get("name")
        )
        db.session.add(new_user)
        db.session.commit()
        return redirect(url_for("get_all_posts"))
    return render_template("register.html", form=form, current_user=current_user)


@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        password = form.password.data
        email = form.email.data
        data = db.session.execute(db.select(User).where(User.email == email))
        user = data.scalar()
        if not user:
            flash("That email does not exist, please try again")
            return redirect("login")
        elif not check_password_hash(user.password, password):
            flash("Password incorrect, please try again.")
            return redirect("login")
        else:
            login_user(user)
            return redirect(url_for("get_all_posts"))
    return render_template("login.html", form=form, current_user=current_user)


